public class Calc {
    
    public static double add(double num1, double num2) {
        return num1 + num2;
    }
    
    public static double sub(double num1, double num2) {
        return num1 - num2;
    }
    
    public static String concat(String str1, String str2) {
        return str1 + " " + str2;
    }
}
